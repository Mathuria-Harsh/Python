# Loops:- It repeats the particular statement till the conditions will satisfied
# There are 2 types:-
#    a) Entry control loop
#    b) Exit control loop
# Steps to follow:-
#     a) start (also called initialization)
#     b) end (also called condition)
#     c) increment

# 1) Entry control loop:- It has only one type of loop

# a) while loop:-
i = 1               #start
while(i<100):       #end
    print(i)        
    i=i+1           #increment 

# reverse operation

i = 10
while(i>=1):
    print(i)
    i=i-1